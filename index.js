function add(a, b){
    return a + b
}

function subtract(b, a){
    return b - a
}

function multiply(a, b){
    return a * b
}

function divide(b, a){
    return b / a
}

function increment(a){
    return ++a
}

function decrement(a){
    return --a
}

function makeInt(n) {
    parseInt(n, 10);
return parseInt(n, 10);
}

function preserveDecimal(n) {
    parseFloat(n);
return parseFloat(n)
}

